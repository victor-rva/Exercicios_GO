// exercício 01:
// Utilizando o operador curto de declaração, atribua estes valores às variáveis com os identificadores "x", "y" e "z".
// 1. 42
// 2. "James Bond"
// 3. true

package main

import "fmt"

func main() {
	x := 42
	y := "James Bond"
	z := true
	fmt.Println(x, y, z)
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}
